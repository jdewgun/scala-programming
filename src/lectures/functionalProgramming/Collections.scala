package lectures.functionalProgramming

object Collections extends App {
  /*
  * Scala offers both immutable and mutable collections
  *
  * Traversable is the mother of all collections
  *
  * Immutable Collections
  * --------------------
  * scala.collections.immutable package
  *
  * Set (HashSet, SortedSet),
  * Map(HashMap, SortedMap),
  * Sequences(
  *   Indexed (Vectors, Ranges, String),
  *   Linear(List, Stream, Stack, Queue)
  * )
  *
  * Mutable Collections
  * -------------------
  * scala.collections.mutable package
  *
  * Set(HashSet, LinkedHashSet)
  * Map(HashMap, MultiMap)
  * Sequence(
  *   IndexedSequence(StringBuilder, ArrayBuffer),
  *   Buffer(ArrayBuffer, ListBuffer)
  *   LinearSequence(LinkedList, MutableList)
  * )
  * */
}
